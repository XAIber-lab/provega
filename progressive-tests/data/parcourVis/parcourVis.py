def load_dataset(filename: str) -> JSon:
    "Load a dataset in json format, potentially bzip2 compressed."
    fopen: Any = open
    if filename.endswith(".bz2"):
        fopen = bz2.open
    with fopen(filename, "rb") as istream:
        return cast(JSon, json.load(istream))


def weighted_choice(cum_weights: List[float]) -> int:
    "Return a random value given a list of cumulative sum of weights"
    rand = random.uniform(sys.float_info.min, cum_weights[-1])
    for index, cum_weight in enumerate(cum_weights):
        if rand <= cum_weight:
            return index
    assert False, "Shouldn't get here"
    return -1


def weighted_average(weights: List[float], min_: float, max_: float) -> float:
    "Return the weighted average, given a histogram and is bounds."
    width: float = (max_ - min_ + 1) / len(weights)
    cum_sum: float = 0
    cum_count: float = 0
    value: float = min_ + width / 2
    for weight in weights:
        cum_sum += weight * value
        cum_count += weight
        value += width
    return cum_sum / cum_count


def weighted_measure(measure: Optional[Dict[str, Any]]) -> Optional[float]:
    "Return a random value generated with a distribution given by a histogram."
    if measure is None:
        return None
    weights: List[float] = measure.get("bins", [])
    range_: Tuple[float, float] = measure.get("range", [0, 1])
    min_ = range_[0]
    max_ = range_[1]
    width: float = (max_ - min_) / len(weights)
    if width == 0:
        width = 1/len(weights)
    cum_weights = list(accumulate(weights))
    rand = weighted_choice(cum_weights)
    return (
        (rand + random.uniform(0, 1)) * width + min_
    )  # return the center of the bin


