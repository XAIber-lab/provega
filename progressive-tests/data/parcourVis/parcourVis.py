def generate(csvfile: _CSVWriter, pid: int, node: Dict[str, Any]) -> None:
    #TODO: Handle the generation of not one disease per patients but of multiple diseases per patients
    events = generate_events(node)
    if len(events) == 0: #This special case can happen because of the threshold set by aggregate.py: The root node can have more "individuals" than the sum of its children count
        return False

    # take the age from the distribution of the last event
    last_disease = ""
    last_event = events[-1]
    last_age = weighted_measure(last_event["node"].get("age_histogram"))
    diseases = last_event["node"].get("disease_barchart", [])
    count = last_event["node"].get("count")
    for cur_disease in diseases:
        rand_count = random.randrange(count)
        if (rand_count < cur_disease['value']):
            if len(last_disease) > 0:
                last_disease += " "
            last_disease += cur_disease['name']

    for event in reversed(events):
        time = weighted_measure(event["node"].get("duration_histogram"))
        event["time"] = time
        category = event["category"]
        if category == "no treatment":
            event["time"] = max(time, 360)
        elif category == "interruption":
            event["time"] = max(time, 180)
        event["age"] = last_age
        last_age -= time / 365.25
        # if last_age is None:
        #     import pdb
        #     pdb.set_trace()
        event["diseases"] = last_disease
        # TODO: update the list of deseases

    rand_date = random.uniform(MIN_DATE.toordinal(), MAX_DATE.toordinal())
    start_date = date.fromordinal(int(rand_date))

    for event in events:
        generate_node(csvfile, pid, start_date, event)
        if "time" in event:
            start_date += timedelta(days=event["time"])

    return True


def generate_events(node: JSon) -> List[Dict[str, Any]]:
    events = []
    while node:
        count = node.get("count", 0)
        if count == 0:
            break
        children = node.get("children", None)
        if children is None:
            break
        weights = [cnt.get("count", 0) for cnt in children]
        cum_weights = list(accumulate(weights))
        cum_weights.append(count)
        # print('cum_weights=', cum_weights)
        rand = weighted_choice(cum_weights)
        if rand >= len(children):
            break
        node = children[rand]
        event = {"category": node["name"], "node": node}
        events.append(event)
    return events


def generate_node(csvfile: _CSVWriter,
                  pid: int,
                  start_date: date,
                  event: Dict[str, Any]) -> None:
    fn = SWITCH.get(event["category"])
    if fn:
        fn(
            csvfile,
            pid,
            start_date,
            str(event["category"]),
            int(event["age"]),
            cast(float, event.get("time")),
            str(event.get("diseases")),
        )
    else:
        assert False, "Received event %s: Shouldn't get here" % event


